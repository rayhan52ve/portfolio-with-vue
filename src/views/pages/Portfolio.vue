<script>
import $ from "jquery";

export default {
  name: "Portfolio",
  props: {
    speed: { type: Number, default: 300 },
    easing: { type: String, default: "ease" },
    hoverDelay: { type: Number, default: 0 },
    inverse: { type: Boolean, default: false },
  },
  data() {
    return {
      transitionProp: "",
      hoverStyle: null,
      hoverTimeout: null,
    };
  },
  computed: {
    transitionCSS() {
      return `all ${this.speed}ms ${this.easing}`;
    },
  },
  mounted() {
    this.$nextTick(() => {
      this.initializeJQueryEvents();
    });

    $(document).on("keyup", this.keyupHandler);
  },
  beforeUnmount() {
    $(document).off("keyup", this.keyupHandler);
  },
  methods: {
    initializeJQueryEvents() {
      $(".slideshow nav span").on("click", this.stopVideos);
      $(".revealator-delay1").addClass("no-transform");

      if ($(".grid").length) {
        new CBPGridGallery(document.getElementById("grid-gallery"));
      }

      $(".grid figure").on("click", () => {
        $("#navbar-collapse-toggle").addClass("hide-header");
      });
      $(".nav-close").on("click", () => {
        $("#navbar-collapse-toggle").removeClass("hide-header");
      });

      $(".nav-prev, .nav-next").on("click", this.toggleHeaderOnNav);
      $(".grid li figure").each((_, el) => {
        $(el).hoverdir();
      });
    },

    stopVideos() {
      const video = document.getElementById("video");
      if (video && !video.paused && !video.ended) {
        video.pause();
      }

      const youtubeVideo = $(".youtube-video")[0];
      if (youtubeVideo) {
        youtubeVideo.contentWindow.postMessage(
          '{"event":"command","func":"pauseVideo","args":""}',
          "*"
        );
      }
    },

    toggleHeaderOnNav() {
      if (
        $(".slideshow ul li:first-child").hasClass("current") ||
        $(".slideshow ul li:last-child").hasClass("current")
      ) {
        $("#navbar-collapse-toggle").removeClass("hide-header");
      } else {
        $("#navbar-collapse-toggle").addClass("hide-header");
      }
    },

    keyupHandler(e) {
      if (e.key === "Escape") {
        this.stopVideos();
        $(".close-content").click();
        $("#navbar-collapse-toggle").removeClass("hide-header");
      } else if (e.key === "ArrowLeft" || e.key === "ArrowRight") {
        this.stopVideos();
      }
    },

    handleMouseEnter(event) {
      this.transitionProp = this.transitionCSS;

      clearTimeout(this.hoverTimeout);
      this.hoverTimeout = setTimeout(() => {
        const direction = this.getDirection(event);
        this.hoverStyle = this.getHoverStyle(direction).to;
      }, this.hoverDelay);
    },

    handleMouseLeave(event) {
      clearTimeout(this.hoverTimeout);
      const direction = this.getDirection(event);
      this.hoverStyle = this.getHoverStyle(direction).from;
    },

    getDirection(event) {
      const { width, height, top, left } = this.$el.getBoundingClientRect();
      const x = (event.clientX - left - width / 2) * (width > height ? height / width : 1);
      const y = (event.clientY - top - height / 2) * (height > width ? width / height : 1);
      const direction = Math.round((Math.atan2(y, x) * (180 / Math.PI) + 180) / 90 + 3) % 4;
      return direction;
    },

    getHoverStyle(direction) {
      const slideFromTop = { left: "0px", top: "-100%" };
      const slideFromBottom = { left: "0px", top: "100%" };
      const slideFromLeft = { left: "-100%", top: "0px" };
      const slideFromRight = { left: "100%", top: "0px" };
      const slideTop = { top: "0px" };
      const slideLeft = { left: "0px" };

      let fromStyle, toStyle;

      switch (direction) {
        case 0:
          fromStyle = !this.inverse ? slideFromTop : slideFromBottom;
          toStyle = slideTop;
          break;
        case 1:
          fromStyle = !this.inverse ? slideFromRight : slideFromLeft;
          toStyle = slideLeft;
          break;
        case 2:
          fromStyle = !this.inverse ? slideFromBottom : slideFromTop;
          toStyle = slideTop;
          break;
        case 3:
          fromStyle = !this.inverse ? slideFromLeft : slideFromRight;
          toStyle = slideLeft;
          break;
      }

      return { from: fromStyle, to: toStyle };
    },
  },
};
</script>

